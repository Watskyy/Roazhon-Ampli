<?php

namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass="App\Repository\SettingRepository")
 */
class Setting
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $property;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $value;

    /**
     * @ORM\ManyToOne(targetEntity="App\Entity\Music", inversedBy="settings")
     * @ORM\JoinColumn(nullable=false)
     */
    private $music;

    /**
     * @ORM\ManyToOne(targetEntity="App\Entity\Ampli", inversedBy="settings")
     * @ORM\JoinColumn(nullable=false)
     */
    private $ampli;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getProperty(): ?string
    {
        return $this->property;
    }

    public function setProperty(string $property): self
    {
        $this->property = $property;

        return $this;
    }

    public function getValue(): ?string
    {
        return $this->value;
    }

    public function setValue(string $value): self
    {
        $this->value = $value;

        return $this;
    }

    public function getMusic(): ?Music
    {
        return $this->music;
    }

    public function setMusic(?Music $music): self
    {
        $this->music = $music;

        return $this;
    }

    public function getAmpli(): ?Ampli
    {
        return $this->ampli;
    }

    public function setAmpli(?Ampli $ampli): self
    {
        $this->ampli = $ampli;

        return $this;
    }
}
